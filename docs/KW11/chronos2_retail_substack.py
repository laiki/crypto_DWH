# chronos_forecast unifies all Chronos forecast cases into one — note how covariates are handled:
# if past and future covariates are identical, pass the same list/dict; otherwise, use separate ones.

def chronos_forecast(Y_train_df,
                     Y_test_df,
                     item_list,
                     prediction_length,
                     quantile_levels,
                     covariates=None,         # None | list | (past_list, future_list) | dict {'past': [...], 'future': [...]}
                     cross_learning=False,
                     verbose=True):
    """
    regrading covariates:
      - None or False -> no covariates
      - list -> same columns used for both past & future covariates
      - tuple/list of length 2 -> (past_cols, future_cols)
      - dict -> may contain keys 'past' and/or 'future' mapping to lists of column names
    """

    def _get_col_lists(cov):
        if not cov:
            return [], []
        # accept a 2-tuple/list as (past_cols, future_cols)
        if isinstance(cov, (list, tuple)) and len(cov) == 2 and all(isinstance(x, (list, tuple)) for x in cov):
            past_cols = list(cov[0])
            future_cols = list(cov[1])
        elif isinstance(cov, dict):
            past_cols = cov.get("past") or cov.get("past_covariates") or []
            future_cols = cov.get("future") or cov.get("future_covariates") or []
        else:
            # single list -> use same for both
            past_cols = list(cov)
            future_cols = list(cov)
        return past_cols, future_cols

    inputs, actuals = [], []
    past_cols, future_cols = _get_col_lists(covariates)

    for _, item in enumerate(item_list):
        item_data_train = Y_train_df[Y_train_df["unique_id"] == item]
        item_data_test = Y_test_df[Y_test_df["unique_id"] == item]

        item_data_train_dict = {"target": item_data_train["y"].to_numpy()}
        item_data_test_dict = {"target": item_data_test["y"].to_numpy()}

        if covariates:
            past_cov_dict = {}
            future_cov_dict = {}

            # Build past covariates from train rows
            for col in past_cols:
                if col not in item_data_train.columns:
                    print(f"[warning] past covariate '{col}' not present for item {item} -> skipping")
                    continue
                past_cov_dict[col] = item_data_train[col].to_numpy()

            # Build future covariates from test rows
            for col in future_cols:
                if col not in item_data_test.columns:
                    print(f"[warning] future covariate '{col}' not present for item {item} -> skipping")
                    continue
                future_cov_dict[col] = item_data_test[col].to_numpy()

            if past_cov_dict:
                item_data_train_dict["past_covariates"] = past_cov_dict
            if future_cov_dict:
                item_data_train_dict["future_covariates"] = future_cov_dict

        inputs.append(item_data_train_dict)
        actuals.append(item_data_test_dict)

    quantiles, mean = pipeline.predict_quantiles(
        inputs,
        prediction_length=prediction_length,
        quantile_levels=quantile_levels,
        predict_batches_jointly=cross_learning
    )
    quantiles = torch.stack(quantiles).squeeze(1)
    mean = torch.stack(mean).squeeze(1)

    if verbose:
        print("Output shapes of each item:", quantiles[0].shape, mean[0].shape)
        print("Total items", len(quantiles))

    return quantiles, mean, inputs, actuals

