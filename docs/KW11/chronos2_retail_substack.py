def cross_validate_fm(
    Y_train_df: pd.DataFrame,
    Y_test_df: pd.DataFrame,
    prediction_length: int,
    context_length: int,
    windows: Optional[int] = None,
    covariates: Optional[object] = None,
    cross_learning: bool = False,
    chronos_forecast_func = None,
    model_prefix: str = "chronos",
    verbose: bool = False,
) -> pd.DataFrame:
    """
    Rolling-origin CV that slices Nixtla-style dataframes per window and calls chronos_forecast once per window.

    context_length: maximum number of past rows per series to keep (train + test-prefix). If a series has fewer rows,
                    all available rows are kept.
    chronos_forecast_func must accept:
      (Y_train_df_w, Y_test_df_w, item_list, covariates, cross_learning, prediction_length, quantile_levels)
    and return (quantiles, mean, inputs, actuals).
    """

    required_cols = {"unique_id", "ds", "y"}
    if not required_cols.issubset(Y_train_df.columns) or not required_cols.issubset(Y_test_df.columns):
        raise ValueError(f"Both Y_train_df and Y_test_df must contain columns {required_cols}")

    if context_length is None or int(context_length) <= 0:
        raise ValueError("context_length must be a positive integer")

    context_length = int(context_length)

    Y_train_df = Y_train_df.sort_values(["unique_id", "ds"]).reset_index(drop=True)
    Y_test_df = Y_test_df.sort_values(["unique_id", "ds"]).reset_index(drop=True)

    series_ids = sorted(Y_test_df["unique_id"].unique())

    # ensure every test series exists in train map (may be empty)
    empty_train_template = pd.DataFrame(columns=Y_train_df.columns)
    train_groups = {uid: grp.sort_values("ds").reset_index(drop=True) for uid, grp in Y_train_df.groupby("unique_id")}
    for uid in series_ids:
        if uid not in train_groups:
            train_groups[uid] = empty_train_template.copy()

    horizons = {uid: len(grp) for uid, grp in Y_test_df.groupby("unique_id")}
    if len(set(horizons.values())) != 1:
        raise ValueError(f"All series must have the same test horizon length. Found per-series lengths: {horizons}")
    total_horizon = next(iter(set(horizons.values())))

    if windows is None:
        windows = math.ceil(total_horizon / prediction_length)
    else:
        windows = int(windows)
        if windows < 1:
            raise ValueError("windows must be >= 1")

    if chronos_forecast_func is None:
        chronos_forecast_func = globals().get("chronos_forecast")
        if chronos_forecast_func is None:
            raise ValueError("chronos_forecast function not provided and not found in globals().")

    records = []
    # We hardcode these here, feel free to change them
    levels = [0.1, 0.5, 0.9]

    for w in tqdm(range(windows), desc="Processing windows"):
        start = w * prediction_length
        remaining = total_horizon - start
        if remaining <= 0:
            break
        horizon_this_window = min(prediction_length, remaining)

        if verbose:
            print(f"[cv] window {w+1}/{windows}: start={start}, horizon={horizon_this_window}")

        # build Y_test_df_w: per-series slice [start : start + horizon_this_window]
        Y_test_df_w = (
            Y_test_df
            .groupby("unique_id", group_keys=False)
            .apply(lambda g: g.iloc[start : start + horizon_this_window])
            .reset_index(drop=True)
        )

        # build test_prefix (to append to train) = per-series test prefix [:start]
        # only the first window will use the Y_train_df as input
        if start == 0:
            combined_train = Y_train_df.copy()
        else:
            test_prefix = (
                Y_test_df
                .groupby("unique_id", group_keys=False)
                .apply(lambda g: g.iloc[:start])
                .reset_index(drop=True)
            )
            combined_train = pd.concat([Y_train_df, test_prefix], axis=0, ignore_index=True)

        # Also, we truncate each series to use the last `context_length` rows
        Y_train_df_w = (
            combined_train
            .sort_values(["unique_id", "ds"])
            .groupby("unique_id", group_keys=False)
            .apply(lambda g: g.iloc[-context_length:])
            .reset_index(drop=True)
        )

        # ensure ordering on both train and test windows
        Y_train_df_w = Y_train_df_w.sort_values(["unique_id", "ds"]).reset_index(drop=True)
        Y_test_df_w = Y_test_df_w.sort_values(["unique_id", "ds"]).reset_index(drop=True)

        # call chronos_forecast once per window
        quantiles_out, mean_out, inputs_out, actuals_out = chronos_forecast_func(
            Y_train_df_w,
            Y_test_df_w,
            item_list=series_ids,
            prediction_length=horizon_this_window,
            quantile_levels=levels,
            covariates=covariates,
            cross_learning=cross_learning,
            verbose=verbose
        )

        q_np = np.asarray(quantiles_out)
        m_np = np.asarray(mean_out)

        if q_np.ndim != 3 or m_np.ndim != 2:
            raise RuntimeError(f"Unexpected shapes from chronos_forecast: quantiles {q_np.shape}, mean {m_np.shape}")

        N, H, Q = q_np.shape
        if H != horizon_this_window:
            raise RuntimeError(f"Predict output horizon H={H} != expected {horizon_this_window}")
        if N != len(series_ids):
            raise RuntimeError(f"Predict output batch size N={N} != number of series {len(series_ids)}")


        q10_arr = q_np[:, :, levels.index(0.1)] # 10-th quantile
        q50_arr = q_np[:, :,  levels.index(0.5)] # median
        q90_arr = q_np[:, :, levels.index(0.9)] # 90-th quantile

        grouped_test_w = {uid: grp.reset_index(drop=True) for uid, grp in Y_test_df_w.groupby("unique_id")}

        for s_idx, uid in enumerate(series_ids):
            test_slice = grouped_test_w[uid]
            ds_arr = test_slice["ds"].to_numpy()
            y_arr = test_slice["y"].to_numpy()
            for h in range(horizon_this_window):
                records.append({
                    "unique_id": uid,
                    "ds": ds_arr[h],
                    "y": float(y_arr[h]),
                    f"{model_prefix}-50": float(q50_arr[s_idx, h]),
                    f"{model_prefix}-10": float(q10_arr[s_idx, h]),
                    f"{model_prefix}-90": float(q90_arr[s_idx, h]),
                })

    out_df = pd.DataFrame.from_records(records)
    out_df = out_df.sort_values(["unique_id", "ds"]).reset_index(drop=True)
    return out_df

