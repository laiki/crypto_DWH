def split_train_test(df, context_length, TEST):

    cutoff = df.sort_values(['unique_id','ds'])['ds'].values[-TEST]
    Y_test_df = df[df.ds >= cutoff].sort_values(['unique_id','ds'])
    Y_train_df = (
        df[df['ds'] < cutoff]
        .sort_values(['unique_id','ds'])
        .groupby('unique_id', group_keys=False)
        .filter(lambda g: len(g) >= context_length)
        .groupby('unique_id', group_keys=False)
        .tail(context_length)
        .reset_index(drop=True)
    )

    if Y_train_df.empty:
        print("Warning: Y_train_df is empty — check context_length or data before cutoff.")
    if Y_test_df.empty:
        print("Warning: Y_test_df is empty — check TEST size or dataset length.")

    return Y_train_df, Y_test_df



def plot_timeseries(inputs, point_forecast, actuals=None, n_series_to_plot=4, intervals=False):
    """
    Plot time series with inputs, forecasts (or quantile intervals), and optional actuals.
    If intervals=True, point_forecast is a quantile tensor (n_series, horizon, n_quantiles).
    Returns a DataFrame with per-series MSE (computed for all series).
    The MSE will be calculated for each series, and n_series_to_plot determines how many of them will be visualized.
    """
    n_series = len(inputs)
    plot_count = min(n_series_to_plot, n_series)
    n_cols = 2 if plot_count > 1 else 1
    n_rows = math.ceil(plot_count / n_cols) if plot_count > 0 else 0

    fig, axes = (plt.subplots(n_rows, n_cols, figsize=(8 * n_cols, 4 * n_rows), squeeze=False) if plot_count > 0 else (None, None))
    results = []

    for idx in range(n_series):
        input_len = len(inputs[idx])

        if intervals:
            q10, q50, q90 = point_forecast[idx][:, 0], point_forecast[idx][:, 4], point_forecast[idx][:, 8]
            forecast_len, pred_mean = len(q50), q50
        else:
            forecast_len, pred_mean = len(point_forecast[idx]), point_forecast[idx]

        actual_len = len(actuals[idx]) if actuals is not None else 0
        mse = (mean_squared_error(actuals[idx][:min(len(pred_mean), len(actuals[idx]))], pred_mean[:min(len(pred_mean), len(actuals[idx]))]) if actuals is not None else np.nan)

        results.append({"series_id": idx, "mse": mse, "input_len": input_len, "forecast_len": forecast_len, "actual_len": actual_len})

        # Plot only subset
        if idx < plot_count:
            ax = axes[idx // n_cols, idx % n_cols]
            ax.plot(range(input_len), inputs[idx], label='Input', color='#1f77b4', marker='o', markersize=3)
            if actuals is not None:
                ax.plot(range(input_len, input_len + actual_len), actuals[idx], label='Actual', color='#2ca02c', marker='x', markersize=4)
            if intervals:
                x = np.arange(input_len, input_len + forecast_len)
                ax.fill_between(x, q10, q90, color='#ff7f0e', alpha=0.25, label='P10–P90 Interval')
                ax.plot(x, q50, color='#B41F77', label='Median (P50)', marker='s', markersize=4)
            else:
                ax.plot(range(input_len, input_len + forecast_len), pred_mean, label='Forecast', color='#B41F77', marker='s', markersize=4)
            ax.set_title(f"Series {idx+1} | MSE={mse:.3f}" if not np.isnan(mse) else f"Series {idx+1}", fontsize=11)
            ax.legend(fontsize=8)
            ax.grid(True, linestyle='--', alpha=0.4)

    # Remove empty subplots
    if plot_count > 0:
        for j in range(plot_count, n_rows * n_cols):
            fig.delaxes(axes[j // n_cols, j % n_cols])
        plt.tight_layout()
        plt.show()
    return pd.DataFrame(results)

